<script>
import { Sticky, Tab, TabItem, Group, XTextarea, Search } from 'vux'
import OrderInfo from '@/components/OrderInfo'
import {User} from '#/storage'
import ENV from 'env'

export default {
  components: {
    Sticky, Tab, TabItem, OrderInfo, Group, XTextarea, Search
  },
  data () {
    return {
      query: {},
      loginUser: {},
      selectedIndex: 0,
      distabdata1: false,
      distabdata2: false,
      distabdata3: false,
      distabdata4: false,
      distabdata5: false,
      tabdata1: [],
      tabdata2: [],
      tabdata3: [],
      tabdata4: [],
      tabdata5: [],
      limit: 10,
      pagestart1: 0,
      pagestart2: 0,
      pagestart3: 0,
      pagestart4: 0,
      pagestart5: 0,
      isLoading1: false,
      isLoading2: false,
      isLoading3: false,
      isLoading4: false,
      isLoading5: false,
      showRefundModal: false,
      refundContent: '',
      clickOrder: {},
      clickIndex: 0,
      pageTop: 0,
      showServiceModal: false,
      serviceContent: '',
      servicePhoto: '',
      servicePhotoArr: [],
      maxnum: 4,
      clickPhotoIndex: undefined,
      searchword1: '',
      autofixed: false
    }
  },
  computed: {
    getList1 () {
      return this.setListButton(this.tabdata1)
    },
    getList2 () {
      return this.setListButton(this.tabdata2)
    },
    getList3 () {
      return this.setListButton(this.tabdata3)
    },
    getList4 () {
      return this.setListButton(this.tabdata4)
    },
    getList5 () {
      return this.setListButton(this.tabdata5)
    }
  },
  filters: {
    addressFormat (address) {
      return `${address.area.join('')}${address.details}`
    }
  },
  watch: {
    selectedIndex () {
      console.log('监控到selectedIndex变化', this.selectedIndex)
      return this.selectedIndex
    }
  },
  methods: {
    initData () {
      this.showRefundModal = false
      this.refundContent = ''
      this.clickOrder = {}
      this.clickIndex = 0
    },
    onSubmit1 () {
      switch (this.selectedIndex) {
        case 0:
          this.distabdata1 = false
          this.tabdata1 = []
          this.pagestart1 = 0
          break
        case 1:
          this.distabdata2 = false
          this.tabdata2 = []
          this.pagestart2 = 0
          break
        case 2:
          this.distabdata5 = false
          this.tabdata5 = []
          this.pagestart5 = 0
          break
        case 3:
          this.distabdata3 = false
          this.tabdata3 = []
          this.pagestart3 = 0
          break
        case 4:
          this.distabdata4 = false
          this.tabdata4 = []
          this.pagestart4 = 0
          break
      }
      this.toggleTab()
    },
    onChange1 (val) {
      this.searchword1 = val
    },
    onCancel1 () {
      this.searchword1 = ''
      switch (this.selectedIndex) {
        case 0:
          this.tabdata1 = []
          this.pagestart1 = 0
          break
        case 1:
          this.tabdata2 = []
          this.pagestart2 = 0
          break
        case 2:
          this.tabdata5 = []
          this.pagestart5 = 0
          break
        case 3:
          this.tabdata3 = []
          this.pagestart3 = 0
          break
        case 4:
          this.tabdata4 = []
          this.pagestart4 = 0
          break
      }
      this.toggleTab()
    },
    deletephoto (index) {
      this.servicePhotoArr.splice(index, 1)
    },
    photoCallback (data) {
      const self = this
      let index = this.clickPhotoIndex
      if (data.flag === 1) {
        if (index !== undefined && index !== 'undefined') {
          self.servicePhotoArr.splice(index, 1, data.data)
        } else {
          self.servicePhotoArr.push(data.data)
        }
      } else if (data.error) {
        self.$vux.toast.show({
          text: data.error,
          time: self.$util.delay(data.error)
        })
      }
    },
    uploadPhoto (refname, index) {
      const self = this
      this.clickPhotoIndex = index
      const fileInput = self.$refs[refname][0] ? self.$refs[refname][0] : self.$refs[refname]
      if (self.$util.isPC()) {
        fileInput.click()
      } else {
        self.$wechat.ready(function () {
          self.$util.wxUploadImage({
            maxnum: self.maxnum,
            handleCallback: function (data) {
              self.photoCallback(data)
            }
          })
        })
      }
    },
    fileChange (refname, index) {
      const self = this
      const target = event.target
      const files = target.files
      if (files.length > 0) {
        let fileForm = target.parentNode
        const filedata = new FormData(fileForm)
        self.$vux.loading.show()
        self.$http.post(`${ENV.BokaApi}/api/upload/files`, filedata).then(res => {
          self.$vux.loading.hide()
          let data = res.data
          self.photoCallback(data)
        })
      }
    },
    toRetailerOrders () {
      let params = this.$util.handleAppParams(this.query, {})
      this.$router.push({path: '/retailerOrders', query: params})
    },
    textareaChange (refname) {
      let curArea = this.$refs[refname][0] ? this.$refs[refname][0] : this.$refs[refname]
      curArea.updateAutosize()
      setTimeout(function () {
        curArea.updateAutosize()
      }, 50)
    },
    textareaFocus (refname) {
      let curArea = this.$refs[refname][0] ? this.$refs[refname][0] : this.$refs[refname]
      curArea.updateAutosize()
    },
    setListButton (list) {
      for (let item of list) {
        switch (item.flag) {
          case 0:
            item.buttons = []
            break
          case 1:
            item.buttons = [
              {id: 1, name: '取消订单'},
              {id: 2, name: '去支付'}
            ]
            break
          case 2:
            if (item.canback && item.backflag !== 20 && (!item.frommin || item.frommin === '' || (item.frommin !== '' && (item.orderonline !== 0 || (item.orderonline === 0 && item.fid))))) {
              item.buttons = [
                {id: 3, name: '申请退款'}
              ]
            }
            break
          case 3:
            // item.buttons = [
            //   {id: 4, name: '查看物流'},
            //   {id: 5, name: '申请售后'},
            //   {id: 6, name: '确认收货'}
            // ]
            let arr = [{id: 4, name: '查看物流'}]
            if (item.canservice) {
              arr.push({id: 5, name: '申请售后'})
            }
            arr.push({id: 6, name: '确认收货'})
            item.buttons = arr
            break
          case 4:
            let arr1 = []
            if (item.canservice) {
              arr1.push({id: 5, name: '申请售后'})
              if (ENV.AllowQuickService) {
                arr1.push({id: 8, name: '完成售后'})
              }
            }
            if (item.backflag === 120) {
              arr1.push({id: 9, name: '查看售后进度'})
            }
            if (item.comment === 0) {
              arr1.push({id: 7, name: '评价'})
            }
            item.buttons = arr1
            break
        }
      }
      return list
    },
    evaluate (order) {
      let params = this.$util.handleAppParams(this.query, {id: order.id})
      this.$router.push({path: '/evaluation', query: params})
    },
    afterConfirm () {
      this.distabdata1 = false
      this.distabdata2 = false
      this.distabdata3 = false
      this.distabdata4 = false
      this.distabdata5 = false
      this.tabdata1 = []
      this.tabdata2 = []
      this.tabdata3 = []
      this.tabdata4 = []
      this.tabdata5 = []
      this.pagestart1 = 0
      this.pagestart2 = 0
      this.pagestart3 = 0
      this.pagestart4 = 0
      this.pagestart5 = 0
      this.toggleTab()
    },
    confirm (order) {
      const self = this
      this.$vux.confirm.show({
        title: '您是否确认收货？',
        content: '请确认货物已收到',
        onConfirm () {
          self.$vux.loading.show()
          self.$http.post(`${ENV.BokaApi}/api/order/receive`, {id: order.id})
          .then(res => {
            self.$vux.loading.hide()
            if (res.data.flag) {
              self.$vux.toast.text(res.data.error)
              // self.changeOrderView(order, 4, [4, 6])
              self.afterConfirm()
            }
          })
        }
      })
    },
    finishService (order) {
      const self = this
      this.$vux.confirm.show({
        title: '您确认结束售后服务？',
        content: '请确认售后结束',
        onConfirm () {
          self.$vux.loading.show()
          self.$http.post(`${ENV.BokaApi}/api/order/finishService`, {id: order.id})
          .then(res => {
            self.$vux.loading.hide()
            if (res.data.flag) {
              self.$vux.toast.text(res.data.error)
              // self.changeOrderView(order, 4, [4, 6])
              self.afterConfirm()
            }
          })
        }
      })
    },
    cancel (order) {
      const self = this
      this.$vux.confirm.show({
        title: '您确认取消订单？',
        onConfirm () {
          self.$vux.loading.show()
          self.$http.post(`${ENV.BokaApi}/api/order/cancel`, {id: order.id})
          .then(res => {
            self.$vux.loading.hide()
            self.$vux.toast.text(res.data.error)
            self.changeOrderView(order, 0, [])
          })
        }
      })
    },
    refund (order, index) {
      this.showRefundModal = true
    },
    closeRefund () {
      this.showRefundModal = false
    },
    submitRefund () {
      const self = this
      self.$vux.loading.show()
      self.$http.post(`${ENV.BokaApi}/api/order/applyRefund`, {id: this.clickOrder.id, reasonreturn: this.refundContent})
      .then(res => {
        self.$vux.loading.hide()
        this.showRefundModal = false
        this.refundContent = ''
        const data = res.data
        self.$vux.toast.show({
          text: data.error,
          type: (data.flag !== 1 ? 'warn' : 'success'),
          time: self.$util.delay(data.error),
          onHide: () => {
            if (data.flag) {
              // self.changeOrderView(this.clickOrder, 0, [])
              switch (this.selectedIndex) {
                case 0:
                  this.tabdata1[this.clickIndex].backflag = 20
                  this.tabdata1[this.clickIndex].flagstr = '待发货(退款申请中)'
                  this.tabdata1[this.clickIndex].buttons = []
                  for (let i = 0; i < this.tabdata2.length; i++) {
                    if (this.tabdata2[i].id === this.clickData.id) {
                      this.tabdata2[i].backflag = 20
                      this.tabdata2[i].flagstr = '待发货(退款申请中)'
                      this.tabdata2[i].buttons = []
                      break
                    }
                  }
                  break
                case 1:
                  this.tabdata2[this.clickIndex].backflag = 20
                  this.tabdata2[this.clickIndex].flagstr = '待发货(退款申请中)'
                  this.tabdata2[this.clickIndex].buttons = []
                  for (let i = 0; i < this.tabdata1.length; i++) {
                    if (this.tabdata1[i].id === this.clickData.id) {
                      this.tabdata1[i].backflag = 20
                      this.tabdata1[i].flagstr = '待发货(退款申请中)'
                      this.tabdata1[i].buttons = []
                      break
                    }
                  }
                  break
              }
            }
          }
        })
      })
    },
    viewShipping (order) {
      if (this.query.fromapp !== 'factory') {
        this.$router.push({path: `/deliverinfo`, query: {id: order.id}})
      } else if (this.query.fromapp === 'factory') {
        this.$router.push({path: `/deliverinfo`, query: {id: order.id, fromapp: 'factory'}})
      }
    },
    closeService () {
      this.showServiceModal = false
      this.serviceContent = ''
      this.servicePhoto = ''
      this.servicePhotoArr = []
    },
    submitService () {
      if (this.$util.trim(this.serviceContent) === '' && this.$util.trim(this.servicePhoto) === '') {
        this.$vux.toast.text('请完善售后信息', 'middle')
        return false
      }
      let sphoto = ''
      if (this.servicePhotoArr.length) {
        sphoto = this.servicePhotoArr.join(',')
      }
      this.$vux.loading.show()
      this.$http.post(`${ENV.BokaApi}/api/order/applyService`, {
        id: this.clickOrder.id, reasonreturn: this.serviceContent, proofphoto: sphoto
      }).then(res => {
        this.$vux.loading.hide()
        const data = res.data
        this.$vux.toast.text(data.error)
        if (data.flag) {
          this.afterConfirm()
          this.showServiceModal = false
          if (this.selectedIndex === 0) {
            this.tabdata1[this.clickIndex].backflag = 120
            this.tabdata1[this.clickIndex].buttons.splice(1, 1)
            for (let i in this.tabdata3) {
              if (this.tabdata3[i].id === this.clickOrder.id) {
                this.tabdata3[i].buttons.splice(1, 1)
              }
            }
          } else {
            this.tabdata3[this.clickIndex].backflag = 120
            this.tabdata3[this.clickIndex].buttons.splice(1, 1)
            for (let i in this.tabdata1) {
              if (this.tabdata1[i].id === this.clickOrder.id) {
                this.tabdata1[i].buttons.splice(1, 1)
              }
            }
          }
        }
      })
    },
    afterSale (order) {
      // 售后
      this.serviceContent = ''
      this.servicePhoto = ''
      this.servicePhotoArr = []
      this.showServiceModal = true
    },
    payment (order) {
      if (this.query.from) {
        this.$wechat.miniProgram.navigateTo({url: `/packageB/pages/pay?id=${order.id}&weburl=orderSearch`})
      } else {
        location.replace(`${ENV.Host}/#/pay?id=${order.id}`)
      }
    },
    orderProcess (type, order, index) {
      this.clickOrder = order
      this.clickIndex = index
      switch (type) {
        case 1:
          this.cancel(order)
          break
        case 2:
          this.payment(order)
          break
        case 3:
          this.refund(order, index)
          break
        case 4:
          this.viewShipping(order)
          break
        case 5:
          this.afterSale(order)
          break
        case 6:
          this.confirm(order)
          break
        case 7:
          this.evaluate(order)
          break
        case 8:
          this.finishService(order)
          break
        case 9:
          let params = this.$util.handleAppParams(this.query, {id: order.id})
          this.$router.push({path: '/orderDetail', query: params})
          break
      }
    },
    changeOrderView (order, status, buttons) {
      const self = this
      let list = []
      switch (this.selectedIndex) {
        case 0:
          list = this.tabdata1
          break
        case 1:
          list = this.tabdata2
          break
        case 2:
          list = this.tabdata5
          break
        case 3:
          list = this.tabdata3
          break
        case 4:
          list = this.tabdata4
          break
      }
      this.$util.changeItem(list, order.id, function (m) {
        let retdata = { ...m, flag: status, flagstr: self.$util.getItem(ENV.OrderStatus, status).status, buttons: buttons }
        console.log(retdata)
        return { ...m, flag: status, flagstr: self.$util.getItem(ENV.OrderStatus, status).status, buttons: buttons }
      })
    },
    toggleTab () {
      console.log('in toggleTab, this.selectedIndex=', this.selectedIndex)
      switch (this.selectedIndex) {
        case 0:
          !this.tabdata1.length && this.getData()
          break
        case 1:
          !this.tabdata2.length && this.getData(2)
          break
        case 2:
          !this.tabdata5.length && this.getData(101)
          break
        case 3:
          !this.tabdata3.length && this.getData(3)
          break
        case 4:
          !this.tabdata4.length && this.getData(4)
          break
      }
    },
    scrollHandle () {
      const self = this
      this.$util.scrollEvent({
        element: this.$refs.scrollContainer,
        callback: () => {
          switch (self.selectedIndex) {
            case 0:
              if (self.tabdata1.length === (self.pagestart1 + 1) * self.limit) {
                self.pagestart1++
                self.getData(0)
              }
              break
            case 1:
              if (self.tabdata2.length === (self.pagestart2 + 1) * self.limit) {
                self.pagestart2++
                self.getData(2)
              }
              break
            case 2:
              if (self.tabdata5.length === (self.pagestart5 + 1) * self.limit) {
                self.pagestart5++
                self.getData(101)
              }
              break
            case 3:
              if (self.tabdata3.length === (self.pagestart3 + 1) * self.limit) {
                self.pagestart3++
                self.getData(3)
              }
              break
            case 4:
              if (self.tabdata4.length === (self.pagestart4 + 1) * self.limit) {
                self.pagestart4++
                self.getData(4)
              }
              break
          }
        }
      })
    },
    getData (flag) {
      if (flag === 2) {
        if (this.isLoading2) return false
        this.isLoading2 = true
      } else if (flag === 3) {
        if (this.isLoading3) return false
        this.isLoading3 = true
      } else if (flag === 4) {
        if (this.isLoading4) return false
        this.isLoading4 = true
      } else if (flag === 101) {
        if (this.isLoading5) return false
        this.isLoading5 = true
      } else {
        if (this.isLoading1) return false
        this.isLoading1 = true
      }
      flag = flag || 0
      console.log('=== 请求中的flag ===')
      console.log(flag)
      this.$vux.loading.show()
      const self = this
      let params = {flag: flag, limit: self.limit}
      if (flag === 2) {
        params.pagestart = this.pagestart2
      } else if (flag === 3) {
        params.pagestart = this.pagestart3
      } else if (flag === 4) {
        params.pagestart = this.pagestart4
      } else if (flag === 101) {
        params.pagestart = this.pagestart5
      } else {
        params.pagestart = this.pagestart1
      }
      if (this.searchword1) {
        params.keyword = this.searchword1
      }
      if (this.query.factoryuid) {
        params.factoryuid = this.query.factoryuid
      }
      this.$http.get(`${ENV.BokaApi}/api/order/orderList/user`, {
        params: params
      }).then((res) => {
        let data = res.data
        self.$vux.loading.hide()
        let retdata = data.data ? data.data : data
        retdata = this.setListButton(retdata)
        switch (flag) {
          case 0:
            this.isLoading1 = false
            console.log('getdata 0')
            self.tabdata1 = self.tabdata1.concat(retdata)
            self.distabdata1 = true
            break
          case 2:
            this.isLoading2 = false
            console.log('getdata 2')
            self.tabdata2 = self.tabdata2.concat(retdata)
            self.distabdata2 = true
            break
          case 3:
            this.isLoading3 = false
            console.log('getdata 3')
            self.tabdata3 = self.tabdata3.concat(retdata)
            self.distabdata3 = true
            break
          case 4:
            this.isLoading4 = false
            console.log('getdata 4')
            self.tabdata4 = self.tabdata4.concat(retdata)
            self.distabdata4 = true
            break
          case 101:
            this.isLoading5 = false
            self.tabdata5 = self.tabdata5.concat(retdata)
            self.distabdata5 = true
            break
        }
      })
    },
    init () {
      this.$vux.loading.show()
    },
    refresh () {
      this.$store.commit('updateToggleTabbar', {toggleTabbar: false})
      this.loginUser = User.get()
      this.initData()
      this.query = this.$route.query
      let flag = 0
      console.log('=== selectedIndex ===')
      console.log(this.selectedIndex)
      console.log(this.query)
      switch (this.selectedIndex) {
        case 1:
          flag = 2
          break
        case 2:
          flag = 101
          break
        case 3:
          flag = 3
          break
        case 4:
          flag = 4
          break
        default :
          flag = 0
          break
      }
      if (this.query.flag) {
        flag = parseInt(this.query.flag)
      }
      console.log('refresh')
      if (flag === 2) {
        if (this.query.refresh || !this.tabdata2.length) {
          console.log('flag=2')
          this.selectedIndex = 1
          this.pagestart2 = 0
          this.tabdata2 = []
          this.toggleTab()
        }
      } else if (flag === 3) {
        if (this.query.refresh || !this.tabdata3.length) {
          console.log('flag=3')
          this.selectedIndex = 3
          this.pagestart3 = 0
          this.tabdata3 = []
          this.toggleTab()
        }
      } else if (flag === 4) {
        if (this.query.refresh || !this.tabdata4.length) {
          console.log('flag=4')
          this.distabdata4 = false
          this.selectedIndex = 4
          this.pagestart4 = 0
          this.tabdata4 = []
          this.toggleTab()
        }
      } else if (flag === 101) {
        if (this.query.refresh || !this.tabdata5.length) {
          console.log('flag=101')
          this.distabdata5 = false
          this.selectedIndex = 2
          this.pagestart5 = 0
          this.tabdata5 = []
          this.toggleTab()
        }
      } else {
        if (this.query.refresh || !this.tabdata1.length) {
          console.log('flag=0')
          this.selectedIndex = 0
          this.pagestart1 = 0
          this.tabdata1 = []
          this.toggleTab()
        }
      }
    }
  },
  created () {
    this.init()
    this.refresh()
  },
  activated () {
    if (this.$refs.scrollContainer) {
      this.$refs.scrollContainer.scrollTop = this.pageTop
    }
    if (document.querySelector('.vux-tab')) {
      document.querySelector('.vux-tab').scrollLeft = this.tabLeft
    }
    this.refresh()
    this.$util.miniPost()
  },
  beforeRouteLeave (to, from, next) {
    if (this.$refs.scrollContainer) {
      this.pageTop = this.$refs.scrollContainer.scrollTop
    }
    if (document.querySelector('.vux-tab')) {
      this.tabLeft = document.querySelector('.vux-tab').scrollLeft
    }
    next()
  }
}
